    private void crearTable() {
        modeloTabla.addColumn("idAlojamiento");
        modeloTabla.addColumn("Localidad");
        modeloTabla.addColumn("Tipo Alojamiento");
        modeloTabla.addColumn("Servicio");
        modeloTabla.addColumn("Precio por DÃ­a");
        alojaTable.setModel(modeloTabla);
    }

    private void llenarTable() {
        List<Alojamiento> lista = new ArrayList<>();
        Disponible_Data inactivas = new Disponible_Data();
        lista = inactivas.listarInactivos();
        for (Alojamiento al : lista) {
            modeloTabla.addRow(new Object[]{al.getIdAlojamiento(), al.getCiudadDest().getNombre(), al.getTipoAlojamiento(), al.getTipoServicio(), al.getImporteDiario()});
        }
    }

    private void ClearTable() {
        for (int i = 0; i < alojaTable.getRowCount(); i++) {
            modeloTabla.removeRow(i);
            i -= 1;
        }
    }
